byte candle_array[] = {CANDLE1_PIN, CANDLE2_PIN, CANDLE3_PIN, CANDLE4_PIN, CANDLE5_PIN,
                       CANDLE6_PIN, CANDLE7_PIN, CANDLE8_PIN};
int candle_state[] = {0, 0, 0, 0, 0, 0, 0, 0};
int candle_count = 0;

//Push buttons
int b_pluss_state = 0;
int b_minus_state = 0;
int b_music_state = 0;
int b_flash_state = 0;

void setup() {
  // initialize the LED pin as an output:
  //  SETUP CANDELS
  pinMode(CANDLE1_PIN, OUTPUT);
  pinMode(CANDLE2_PIN, OUTPUT);
  pinMode(CANDLE3_PIN, OUTPUT);
  pinMode(CANDLE4_PIN, OUTPUT);
  pinMode(CANDLE5_PIN, OUTPUT);
  pinMode(CANDLE6_PIN, OUTPUT);
  pinMode(CANDLE7_PIN, OUTPUT);
  pinMode(CANDLE8_PIN, OUTPUT);
  pinMode(CANDLE_SHAMASH_PIN, OUTPUT);

  //  SETUP BUTTONS
  pinMode(B_PLUSS_PIN, INPUT);
  pinMode(B_MINUS_PIN, INPUT);
  pinMode(B_FLASH_PIN, INPUT);

  //  SETUP TONE
  pinMode(MELODY_PIN, OUTPUT);
}

void loop() {
  
  //  read user push buttons command
  b_pluss_state = digitalRead(B_PLUSS_PIN);
  b_minus_state = digitalRead(B_MINUS_PIN);
  b_music_state = digitalRead(B_MUSIC_PIN);
  b_flash_state = digitalRead(B_FLASH_PIN);

  if (b_pluss_state == HIGH)
  {
    candle_count++;
  if (candle_count > 8)
    candle_count = 8;
    delay (50);
  }

  if (b_minus_state == HIGH)
  {
    candle_count--;
    if (candle_count < 0)
      candle_count = 0;
    delay (50);
  }
  
  if (b_music_state == HIGH)
  {
    //  TODO implement state
    delay (50);
  }
  
  if (b_flash_state == HIGH)
  {
    //  TODO implement state
    delay (50);
  }


  //update led candle array  
  for (int i = 0; i < CANDELS; i++)
  {     
    candle_state[i] = LOW;
    
  if (i < candle_count)
    candle_state[i] = HIGH;
  }

  // write led candle array
  digitalWrite(CANDLE_SHAMASH_PIN, HIGH);
  for (int i = 0; i < CANDELS; i++)
  {
    digitalWrite(candle_array[i], candle_state[i]);
  }

  // play music set
  switch (candle_count)
  {
    case 0:
      midi0();
    candle_count = 9; // go to silent mode next iteration
      break;

    case 1:
      midi1();
      break;

    case 2:
      midi2();
      break;

    case 3:
      midi3();
      break;

    case 4:
      midi4();
      break;

    case 5:
      midi5();
      break;

    case 6:
      midi6();
      break;

    case 7:
      midi7();
      break;

    case 8:
      midi8();
      break;
    
  case 9:
  //silent 
    break;
    
    default:
      midi0();
      midi0();
  }
}

int my_delay(float time_delay)
{
  b_pluss_state = digitalRead(B_PLUSS_PIN);
  b_minus_state = digitalRead(B_MINUS_PIN);
  b_flash_state = digitalRead(B_FLASH_PIN);
  if (b_pluss_state == HIGH |b_minus_state == HIGH | b_flash_state == HIGH)
    return 1;
  delay(time_delay);
  return 0;
    
}

